public class nestedLoops {

    public static void main(String[] args)
    {
        //Nested Loop is basically Loop inside another loop


        for (int i = 1 ; i <= 3 ; i ++){
            for (int j = 1 ; j < 10 ; j++){
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
